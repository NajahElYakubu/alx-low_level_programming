#include "3-calc.h"
/**
 * op_add - function that returns sum
 * @a: number
 * @b: number
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that returns difference
 * Return: result
 * @a: number
 * @b: number
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that returns product#
 * Return: result
 * @a: number
 * @b: number
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that returns division
 * Return: result
 * @a: number
 * @b: number
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that returns remainder of division
 * Return: result
 * @a: number
 * @b: number
 */
int op_mod(int a, int b)
{
	return (a % b);
}
